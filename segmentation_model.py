        "        root_dir    = ROOT_DIR,\n",
        "        batch_size  = 8,\n",
        "        num_workers = 2,\n",
        "        pin_memory  = torch.cuda.is_available(),\n",
        "    )\n",
        "\n",
        "    # ── 2. Inspect a single batch ─────────────────────────────\n",
        "    images, masks = next(iter(train_loader))\n",
        "\n",
        "    print(f\"\\n── Batch inspection ──────────────────────────\")\n",
