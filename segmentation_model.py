        "import os\n",
        "import numpy as np\n",
        "from PIL import Image\n",
        "from tqdm import tqdm\n",
        "\n",
        "import torch\n",
        "from torch.utils.data import Dataset, DataLoader\n",
        "\n",
        "import albumentations as A\n",
        "from albumentations.pytorch import ToTensorV2\n",
        "import os\n",
        "os.makedirs(\"/content/outputs\", exist_ok=True)\n",
        "\n",
        "# ── CLASS ID → SEQUENTIAL LABEL MAPPING ─────────────────────\n",
        "# Raw mask pixel values → contiguous 0-based indices for CrossEntropyLoss\n",
        "CLASS_ID_TO_LABEL = {\n",
        "    100:   0,   # Trees\n",
        "    200:   1,   # Lush Bushes\n",
        "    300:   2,   # Dry Grass\n",
        "    500:   3,   # Dry Bushes\n",
        "    550:   4,   # Ground Clutter\n",
        "    600:   5,   # Flowers\n",
        "    700:   6,   # Logs\n",
        "    800:   7,   # Rocks\n",
        "    7100:  8,   # Landscape\n",
        "    10000: 9,   # Sky\n",
        "}\n",
        "\n",
        "# Human-readable class names (useful for visualisation / metrics)\n",
        "CLASS_NAMES = [\n",
        "    \"Trees\",         # 0\n",
        "    \"Lush Bushes\",   # 1\n",
        "    \"Dry Grass\",     # 2\n",
        "    \"Dry Bushes\",    # 3\n",
        "    \"Ground Clutter\",# 4\n",
        "    \"Flowers\",       # 5\n",
        "    \"Logs\",          # 6\n",
        "    \"Rocks\",         # 7\n",
        "    \"Landscape\",     # 8\n",
        "    \"Sky\",           # 9\n",
        "]\n",
        "\n",
        "NUM_CLASSES = len(CLASS_NAMES)   # 10\n",
        "\n",
        "# ImageNet mean & std — used for normalising RGB images\n",
        "IMAGENET_MEAN = (0.485, 0.456, 0.406)\n",
        "IMAGENET_STD  = (0.229, 0.224, 0.225)\n",
        "\n",
        "IMAGE_SIZE = 512   # both H and W are resized to this value\n",
        "\n",
        "\n",
        "# ── DATASET CLASS ────────────────────────────────────────────\n",
        "class DesertSegmentationDataset(Dataset):\n",
        "    \"\"\"\n",
        "    PyTorch Dataset for the Desert Environment Segmentation task.\n",
        "\n",
        "    Directory layout expected:\n",
        "        root_dir/\n",
        "            train/\n",
        "                images/   *.jpg / *.png\n",
        "                masks/    *.png  (grayscale, raw class IDs)\n",
        "            val/\n",
        "                images/\n",
        "                masks/\n",
        "            test/\n",
        "                images/   (masks optional for test split)\n",
        "                masks/\n",
        "\n",
        "    Args:\n",
        "        root_dir  (str)            : Path to the dataset root.\n",
        "        split     (str)            : One of \"train\", \"val\", \"test\".\n",
        "        transform (A.Compose|None) : Albumentations pipeline.\n",
        "                                     If None a safe default is applied.\n",
        "    \"\"\"\n",
        "\n",
        "    def __init__(self, root_dir: str, split: str, transform=None):\n",
        "        assert split in (\"train\", \"val\", \"test\"), \\\n",
        "            f\"split must be 'train', 'val', or 'test', got '{split}'\"\n",
        "\n",
        "        self.split      = split\n",
        "        self.transform  = transform\n",
        "        self.id_to_label = CLASS_ID_TO_LABEL\n",
        "\n",
        "        self.image_dir = os.path.join(root_dir, split, \"Color_Images\")\n",
        "        self.mask_dir  = os.path.join(root_dir, split, \"Segmentation\")\n",
        "\n",
        "        if not os.path.isdir(self.image_dir):\n",
        "            raise FileNotFoundError(f\"Image directory not found: {self.image_dir}\")\n",
        "\n",
        "        # ── collect filenames ────────────────────────────────\n",
        "        valid_exts = {\".jpg\", \".jpeg\", \".png\", \".bmp\", \".tif\", \".tiff\"}\n",
        "        self.image_names = sorted(\n",
        "            f for f in os.listdir(self.image_dir)\n",
        "            if os.path.splitext(f)[1].lower() in valid_exts\n",
        "        )\n",
        "\n",
        "        if len(self.image_names) == 0:\n",
        "            raise RuntimeError(f\"No images found in {self.image_dir}\")\n",
        "\n",
        "        # Build a lookup table (LUT) once for fast numpy remapping:\n",
        "        #   lut[raw_id] = sequential_label\n",
        "        # Raw IDs go up to 10 000, so a 10 001-element array is enough.\n",
        "        max_id = max(self.id_to_label.keys()) + 1   # 10 001\n",
        "        self._lut = np.zeros(max_id, dtype=np.int64)\n",
        "        for raw_id, label in self.id_to_label.items():\n",
        "            self._lut[raw_id] = label\n",
        "\n",
        "    # ── helpers ──────────────────────────────────────────────\n",
        "\n",
        "    def _remap_mask(self, mask_np: np.ndarray) -> np.ndarray:\n",
        "        \"\"\"\n",
        "        Convert a 2-D array of raw class IDs to sequential labels\n",
        "        using the pre-built LUT (vectorised, no Python loops).\n",
        "        Pixels with unknown IDs silently map to 0.\n",
        "        \"\"\"\n",
        "        # Clip to LUT bounds so unknown IDs don't cause index errors\n",
        "        safe = np.clip(mask_np.astype(np.int64), 0, len(self._lut) - 1)\n",
        "        return self._lut[safe]\n",
        "\n",
        "    def _stem(self, image_name: str) -> str:\n",
        "        \"\"\"Return the filename without extension.\"\"\"\n",
        "        return os.path.splitext(image_name)[0]\n",
        "\n",
        "    def _find_mask_path(self, image_name: str) -> str | None:\n",
        "        \"\"\"\n",
        "        Try to locate the matching mask file.\n",
        "        Falls back gracefully for test splits that may lack masks.\n",
        "        \"\"\"\n",
        "        stem = self._stem(image_name)\n",
        "        for ext in (\".png\", \".jpg\", \".jpeg\", \".bmp\", \".tif\", \".tiff\"):\n",
        "            candidate = os.path.join(self.mask_dir, stem + ext)\n",
        "            if os.path.isfile(candidate):\n",
        "                return candidate\n",
        "        return None   # mask not found (acceptable for test split)\n",
        "\n",
        "    # ── Dataset interface ────────────────────────────────────\n",
        "\n",
        "    def __len__(self) -> int:\n",
        "        return len(self.image_names)\n",
        "\n",
        "    def __getitem__(self, idx: int):\n",
        "        image_name = self.image_names[idx]\n",
        "\n",
        "        # ── load image (H×W×3, uint8) ────────────────────────\n",
        "        image_path = os.path.join(self.image_dir, image_name)\n",
        "        image = np.array(\n",
        "            Image.open(image_path).convert(\"RGB\"),\n",
        "            dtype=np.uint8\n",
        "        )\n",
        "\n",
        "        # ── load mask (H×W, uint16/int32 raw IDs) ───────────\n",
        "        mask_path = self._find_mask_path(image_name)\n",
        "\n",
        "        if mask_path is not None:\n",
        "            # Read as-is (grayscale) to preserve large pixel values\n",
        "            raw_mask = np.array(\n",
        "                Image.open(mask_path).convert(\"I\"),   # 32-bit grayscale\n",
        "                dtype=np.int32\n",
        "            )\n",
        "            mask = self._remap_mask(raw_mask).astype(np.int64)\n",
        "        else:\n",
        "            # Test images without a mask: return a blank mask\n",
        "            mask = np.zeros(image.shape[:2], dtype=np.int64)\n",
        "\n",
        "        # ── resize to 512×512 before augmentation ───────────\n",
        "        # Use PIL for high-quality resizing, then convert back to numpy\n",
        "        image = np.array(\n",
        "            Image.fromarray(image).resize(\n",
        "                (IMAGE_SIZE, IMAGE_SIZE), Image.BILINEAR\n",
        "            ),\n",
        "            dtype=np.uint8\n",
        "        )\n",
        "        mask = np.array(\n",
        "            Image.fromarray(mask.astype(np.int32), mode=\"I\").resize(\n",
        "                (IMAGE_SIZE, IMAGE_SIZE), Image.NEAREST   # no interpolation on labels\n",
        "            ),\n",
        "            dtype=np.int64\n",
        "        )\n",
        "\n",
        "        # ── apply albumentations transform ───────────────────\n",
        "        if self.transform is not None:\n",
        "            augmented = self.transform(image=image, mask=mask)\n",
        "            image = augmented[\"image\"]   # float32 tensor after ToTensorV2\n",
        "            mask  = augmented[\"mask\"]    # long tensor\n",
        "        else:\n",
        "            # Minimal safe fallback: just to-tensor + normalise\n",
        "            _fallback = A.Compose([\n",
        "                A.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),\n",
        "                ToTensorV2(),\n",
        "            ])\n",
        "            augmented = _fallback(image=image, mask=mask)\n",
        "            image = augmented[\"image\"]\n",
        "            mask  = augmented[\"mask\"]\n",
        "\n",
        "        # Ensure correct dtypes\n",
        "        image = image.float()           # (3, H, W) float32\n",
        "        mask  = mask.long()             # (H, W)    int64\n",
        "\n",
        "        return image, mask\n",
        "\n",
        "    def __repr__(self) -> str:\n",
        "        return (\n",
        "            f\"DesertSegmentationDataset(\"\n",
        "            f\"split='{self.split}', \"\n",
        "            f\"n_samples={len(self)}, \"\n",
        "            f\"n_classes={NUM_CLASSES})\"\n",
        "        )\n",
        "\n",
        "\n",
        "# ── AUGMENTATION PIPELINES ───────────────────────────────────\n",
        "\n",
        "def get_train_transforms() -> A.Compose:\n",
        "    \"\"\"\n",
        "    Strong augmentation for training:\n",
        "      spatial  → random crop, horizontal flip, rotation\n",
        "      colour   → brightness / contrast / saturation / hue jitter\n",
        "      finalise → normalise (ImageNet stats) + convert to tensor\n",
        "    \"\"\"\n",
        "    return A.Compose([\n",
        "        # ── spatial augmentations ────────────────────────────\n",
        "        A.HorizontalFlip(p=0.5),\n",
        "        A.RandomRotate90(p=0.3),\n",
        "        A.Rotate(limit=15, border_mode=0, p=0.4),       # up to ±15°\n",
        "        A.RandomResizedCrop(\n",
        "            size=(IMAGE_SIZE, IMAGE_SIZE),\n",
        "            scale=(0.7, 1.0),    # crop between 70 % and 100 % of the image\n",
        "            ratio=(0.75, 1.33),\n",
        "            p=0.5,\n",
        "        ),\n",
        "\n",
        "        # ── colour / texture augmentations ───────────────────\n",
        "        A.ColorJitter(\n",
        "            brightness=0.3,\n",
        "            contrast=0.3,\n",
        "            saturation=0.3,\n",
        "            hue=0.1,\n",
        "            p=0.6,\n",
        "        ),\n",
        "        A.RandomGamma(gamma_limit=(80, 120), p=0.3),\n",
        "        A.GaussNoise(var_limit=(10.0, 50.0), p=0.2),\n",
        "        A.GaussianBlur(blur_limit=(3, 5), p=0.2),\n",
        "\n",
        "        # ── normalise + to tensor ─────────────────────────────\n",
        "        A.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),\n",
        "        ToTensorV2(),\n",
        "    ])\n",
        "\n",
        "\n",
        "def get_val_transforms() -> A.Compose:\n",
        "    \"\"\"\n",
        "    Minimal transform for validation / inference:\n",
        "    only normalise (resize already done inside __getitem__).\n",
        "    \"\"\"\n",
        "    return A.Compose([\n",
        "        A.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),\n",
        "        ToTensorV2(),\n",
        "    ])\n",
        "\n",
        "\n",
        "# ── DATALOADER FACTORY ───────────────────────────────────────\n",
        "\n",
        "def get_dataloaders(\n",
        "    root_dir:   str,\n",
        "    batch_size: int = 8,\n",
        "    num_workers: int = 2,\n",
        "    pin_memory: bool = True,\n",
        ") -> tuple[DataLoader, DataLoader]:\n",
        "    \"\"\"\n",
        "    Build and return (train_loader, val_loader).\n",
        "\n",
        "    Args:\n",
        "        root_dir    : Path to the dataset root directory.\n",
        "        batch_size  : Samples per batch (default 8).\n",
        "        num_workers : Parallel data-loading workers (default 2).\n",
        "        pin_memory  : Set True when training on a CUDA GPU.\n",
        "\n",
        "    Returns:\n",
        "        (train_loader, val_loader) as a tuple of DataLoader objects.\n",
        "    \"\"\"\n",
        "\n",
        "    # ── datasets ─────────────────────────────────────────────\n",
        "    train_dataset = DesertSegmentationDataset(\n",
        "        root_dir  = root_dir,\n",
        "        split     = \"train\",\n",
        "        transform = get_train_transforms(),\n",
        "    )\n",
        "\n",
        "    val_dataset = DesertSegmentationDataset(\n",
        "        root_dir  = root_dir,\n",
        "        split     = \"val\",\n",
        "        transform = get_val_transforms(),\n",
        "    )\n",
        "\n",
        "    print(f\"📂 {train_dataset}\")\n",
        "    print(f\"📂 {val_dataset}\")\n",
        "\n",
        "    # ── loaders ──────────────────────────────────────────────\n",
        "    train_loader = DataLoader(\n",
        "        train_dataset,\n",
        "        batch_size  = batch_size,\n",
        "        shuffle     = True,         # shuffle every epoch during training\n",
        "        num_workers = num_workers,\n",
        "        pin_memory  = pin_memory,\n",
        "        drop_last   = True,         # avoids batch-norm issues on tiny last batch\n",
        "    )\n",
        "\n",
        "    val_loader = DataLoader(\n",
        "        val_dataset,\n",
        "        batch_size  = batch_size,\n",
        "        shuffle     = False,        # keep order stable for reproducible metrics\n",
        "        num_workers = num_workers,\n",
        "        pin_memory  = pin_memory,\n",
        "        drop_last   = False,\n",
        "    )\n",
        "\n",
        "    print(f\"\\n✅ Train batches : {len(train_loader)}\")\n",
        "    print(f\"✅ Val   batches : {len(val_loader)}\")\n",
        "\n",
        "    return train_loader, val_loader\n",
        "\n",
        "\n",
        "# ── EXAMPLE USAGE ────────────────────────────────────────────\n",
        "if __name__ == \"__main__\":\n",
        "    import matplotlib.pyplot as plt\n",
        "\n",
        "    ROOT_DIR = \"/content/drive/MyDrive/Offroad_Segmentation_Training_Dataset\"\n",
